const TURN_PREFIX_SUMMARIZATION_PROMPT = `This is the PREFIX of a turn that was too large to keep. The SUFFIX (recent work) is retained.

Summarize the prefix to provide context for the retained suffix:

## Original Request
[What did the user ask for in this turn?]

## Early Progress
- [Key decisions and work done in the prefix]

## Context for Suffix
- [Information needed to understand the retained recent work]

Be concise. Focus on what's needed to understand the kept suffix.`;

/**
 * Below this many chars of explanatory prose in the compacted window, skip the
 * summarization LLM entirely and emit only the deterministic structural frame
 * (file operations + digests). A window that is a pure burst of tool I/O —
 * back-to-back reads/greps/edits with no assistant narration and no user prose —
 * has nothing for the summarizer to add over the structural lists, so the LLM
 * call (and its self-correction pass) is wasted latency and tokens. The bar is
 * deliberately small: ANY non-trivial prose tips the window back to the LLM path
 * so context quality is never degraded. Forced always-LLM via
 * PIT_NO_STRUCTURAL_COMPACTION=1.
 */
const STRUCTURAL_ONLY_PROSE_THRESHOLD = 200;

/**
 * Count chars of explanatory prose in the compacted window: assistant `text`
 * blocks, user free-text, and non-relay `custom` (extension-injected) text —
 * each as string content or `text`-type content blocks. Custom text IS counted
 * because convertToLlm relays it to the summarizer as a user message (a relay
 * custom — customType === MESSAGE_RELAY_CUSTOM_TYPE — is excluded, since
 * convertToLlm drops it, so it never reaches the model). Deliberately EXCLUDES
 * tool calls, tool results, thinking, bash output and the structured fields of
 * system/branch/compaction messages — those are mechanical and already captured
 * deterministically by the structural frame. This measures only what a
 * summarizer could add over the structural lists.
 */
function measureProseChars(messages: AgentMessage[]): number {
	let prose = 0;
	for (const message of messages) {
		if (message.role === "assistant") {
			for (const block of message.content) {
				if (block.type === "text") prose += block.text.length;
			}
			continue;
		}
		if (message.role === "user") {
			const content = (message as { content: string | Array<{ type: string; text?: string }> }).content;
			if (typeof content === "string") {
				prose += content.length;
			} else if (Array.isArray(content)) {
				for (const block of content) {
					if (block.type === "text" && block.text) prose += block.text.length;
				}
			}
			continue;
		}
		if (message.role === "custom") {
			// Relay custom messages are display-only — convertToLlm drops them, so
			// they carry no prose the summarizer would ever see.
			if (message.customType === MESSAGE_RELAY_CUSTOM_TYPE) continue;
			const content = message.content;
			if (typeof content === "string") {
				prose += content.length;
			} else if (Array.isArray(content)) {
				for (const block of content) {
					if (block.type === "text" && block.text) prose += block.text.length;
				}
			}
		}
	}
	return prose;
}

/**
 * True when the window has so little explanatory prose that the structural frame
 * already captures everything a summary could. Gated off by
 * PIT_NO_STRUCTURAL_COMPACTION=1 (forces the always-LLM path). A previousSummary
 * present means this is an INCREMENTAL compaction whose prose lives in the prior
 * summary, not the window — never collapse to structural-only there, or that
 * prose would be silently dropped.
 */
function shouldUseStructuralOnly(
	messagesToSummarize: AgentMessage[],
	turnPrefixMessages: AgentMessage[],
	previousSummary: string | undefined,
): boolean {
	if (isTruthyEnvFlag(process.env.PIT_NO_STRUCTURAL_COMPACTION)) return false;
	if (previousSummary) return false;
	const prose = measureProseChars(messagesToSummarize) + measureProseChars(turnPrefixMessages);
	return prose < STRUCTURAL_ONLY_PROSE_THRESHOLD;
}

/** Deterministic header emitted in place of an LLM summary for tool-only windows. */
const STRUCTURAL_ONLY_HEADER = `## Goal
(No explanatory prose in this window — mechanical tool activity only. See operations below.)

## Progress
- Tool operations on the listed files/searches/commands were applied. The retained recent context continues the work.`;

/**
 * Living-repo-map preseed + buildFileDigests over the touched files. Never
 * called with an empty `digestPaths`. Extracted from compact() so the digest
 * I/O (disk reads + symbol parse) can run CONCURRENTLY with the summarization
 * LLM calls — the inputs depend only on the preparation's fileOps, not on the
 * summary text.
 */
async function collectFileDigests(
	digestPaths: string[],
	cwd: string | undefined,
	signal: AbortSignal | undefined,
): Promise<Record<string, string>> {
	// Living repo map: a git-anchored incremental symbol index. Project it onto the
	// touched files and pass as a pre-seed so anything it already indexed is NOT
	// re-read/re-parsed here — the digest becomes a cache READ. Fail-open: any
	// failure leaves preSeed undefined and buildFileDigests rebuilds from disk
	// (output is identical; only the cost differs).
	let preSeed: Record<string, string> | undefined;
	try {
		const { map } = await getLivingRepoMap(cwd ?? ".");
		const seed: Record<string, string> = {};
		const relKeys = digestPaths.map((p) =>
			relative(cwd ?? ".", isAbsolute(p) ? p : resolve(cwd ?? ".", p))
				.split("\\")
				.join("/"),
		);
		// Filter the living-repo-map projection to just the touched files so we don't
		// materialize a digest string for every indexed file in the repo. The lookup
		// keys (and resulting seed) are identical; only the intermediate dict shrinks.
		const mapDigests = livingRepoMapToDigests(map, relKeys);
		for (let i = 0; i < digestPaths.length; i++) {
			const symbols = mapDigests[relKeys[i]];
			if (symbols !== undefined) seed[digestPaths[i]] = symbols;
		}
		preSeed = seed;
	} catch {
		preSeed = undefined;
	}
	return buildFileDigests(
		digestPaths,
		async (p) => {
			try {
				const fullPath = isAbsolute(p) ? p : resolve(cwd ?? ".", p);
				const st = await stat(fullPath);
				if (st.size > MAX_DIGEST_BYTES) return null;
				return await readFile(fullPath, { encoding: "utf8", signal });
			} catch {
				return null;
			}
		},
		signal,
		preSeed,
	);
}

/**
 * Generate summaries for compaction using prepared data.
 * Returns CompactionResult - SessionManager adds uuid/parentUuid when saving.
 *
 * @param preparation - Pre-calculated preparation from prepareCompaction()
 * @param customInstructions - Optional custom focus for the summary
 */
export async function compact(
	preparation: CompactionPreparation,
	model: Model<any>,
	apiKey: string | undefined,
	headers?: Record<string, string>,
	customInstructions?: string,
	signal?: AbortSignal,
	thinkingLevel?: ThinkingLevel,
	streamFn?: StreamFn,
): Promise<CompactionResult> {
	const {
		firstKeptEntryId,
		messagesToSummarize: messagesToSummarizeRaw,
		turnPrefixMessages: turnPrefixMessagesRaw,
		isSplitTurn,
		tokensBefore,
		previousSummary,
		fileOps,
		settings,
		cwd,
	} = preparation;

	// Clone toolResult messages BEFORE pruning. getMessageFromEntry returns
	// entry.message by reference for 'message' entries, so without this the prune
	// would elide tool results inside the LIVE session context; an aborted
	// summarization would then leave them elided with no restore. Cloning makes the
	// prune operate on throw-away copies — happy-path output is unchanged.
	const messagesToSummarize = cloneToolResultMessagesForPrune(messagesToSummarizeRaw);
	const turnPrefixMessages = isSplitTurn
		? cloneToolResultMessagesForPrune(turnPrefixMessagesRaw)
		: turnPrefixMessagesRaw;

	// Pre-prune large tool outputs before sending to summarizer. Scale the
	// threshold to the window: tighter windows prune more aggressively (capped at
	// the default so large windows are unchanged).
	const pruneThreshold = Math.min(
		PRUNE_TOKEN_THRESHOLD,
		Math.max(4_000, Math.floor((model.contextWindow || 200_000) * 0.1)),
	);
	pruneOldToolOutputs(messagesToSummarize, pruneThreshold);
	if (isSplitTurn) {
		pruneOldToolOutputs(turnPrefixMessages, pruneThreshold);
	}

	// Serialized views shared by the summarizer and the verification pass.
	// Created AFTER the prune above — the prune rewrites block.text in place, and
	// the memo must capture the pruned (final) window. One window per message
	// list; never share across main/prefix.
	const mainWindow = createSerializedWindow(messagesToSummarize);
	const prefixWindow = isSplitTurn ? createSerializedWindow(turnPrefixMessages) : undefined;

	// File digests kick off NOW so the disk reads + symbol parsing overlap the
	// summarization LLM calls below; the result is awaited only after the verify
	// pass. Reading at compaction start instead of after the LLM round-trips is
	// equivalent: the session is disconnected/idle during compaction, so no tool
	// writes land in between.
	const lists = computeOperationLists(fileOps, cwd);
	// Modified files are the artifact trail that summary prose silently drops, so
	// they get digests by default; read-only files stay behind PIT_FILE_DIGESTS to
	// bound prefix growth. The lists are disjoint (read AND modified surfaces only
	// in modifiedFiles), so concatenating needs no dedup.
	const digestPaths = isTruthyEnvFlag(process.env.PIT_FILE_DIGESTS)
		? [...lists.modifiedFiles, ...lists.readFiles]
		: lists.modifiedFiles;
	const digestsPromise = digestPaths.length > 0 ? collectFileDigests(digestPaths, cwd, signal) : undefined;
	// No-op branch so a summarization failure below cannot surface an unhandled
	// rejection from the in-flight digests; the real `await digestsPromise` after
	// the verify pass still observes (and rethrows) any digest error.
	digestsPromise?.catch(() => {});

	// Generate summaries (can be parallel if both needed) and merge into one
	let summary: string;

	// Structural-only fast path: when the compacted window is a pure burst of tool
	// I/O with no explanatory prose, the deterministic operation lists + digests
	// (appended below) already capture everything a summary could. Skip the
	// summarizer LLM and its self-correction pass. Disabled by
	// PIT_NO_STRUCTURAL_COMPACTION=1 and never taken for incremental compactions
	// (see shouldUseStructuralOnly).
	const structuralOnly = shouldUseStructuralOnly(messagesToSummarize, turnPrefixMessages, previousSummary);
	if (structuralOnly) {
		summary = STRUCTURAL_ONLY_HEADER;
	} else if (isSplitTurn && turnPrefixMessages.length > 0) {
		const [historyResult, turnPrefixResult] = await Promise.all([
			messagesToSummarize.length > 0
				? generateSummary(
						messagesToSummarize,
						model,
						settings.reserveTokens,
						apiKey,
						headers,
						signal,
						customInstructions,
						previousSummary,
						thinkingLevel,
						streamFn,
						mainWindow,
					)
				: Promise.resolve("No prior history."),
			generateTurnPrefixSummary(
				turnPrefixMessages,
				model,
				settings.reserveTokens,
				apiKey,
				headers,
				signal,
				thinkingLevel,
				streamFn,
				prefixWindow,
			),
		]);
		summary = `${historyResult}\n\n---\n\n**Turn Context (split turn):**\n\n${turnPrefixResult}`;
	} else {
		summary = await generateSummary(
			messagesToSummarize,
			model,
			settings.reserveTokens,
			apiKey,
			headers,
			signal,
			customInstructions,
			previousSummary,
			thinkingLevel,
			streamFn,
			mainWindow,
		);
	}

	// Self-correction: verify summary for omitted details. Gated by input size —
	// small/incremental compactions skip the second LLM call (low omission risk).
	const verifyInputTokens =
		sumMessageTokens(messagesToSummarize) + (isSplitTurn ? sumMessageTokens(turnPrefixMessages) : 0);
	if (!structuralOnly && settings.selfCorrection !== false && verifyInputTokens >= VERIFY_MIN_INPUT_TOKENS) {
		const maxTokens = summarizationMaxTokens(model, settings.reserveTokens, 0.8);
		const verifySource = buildVerificationSource(
			messagesToSummarize,
			isSplitTurn ? turnPrefixMessages : [],
			mainWindow,
			isSplitTurn ? prefixWindow : undefined,
		);
		summary = await verifySummary(
			summary,
			verifySource,
			model,
			maxTokens,
			apiKey,
			headers,
			signal,
			thinkingLevel,
			streamFn,
		);
	}

	if (!structuralOnly && !isTruthyEnvFlag(process.env.PIT_NO_COMPACT_SUMMARY_OUTPUT)) {
		summary = trimSummaryProseAgainstOperations(summary, lists);
	}
	// Deterministic grounding: annotate path tokens in the prose that are neither
	// in the operation lists nor on disk with `(unverified)`. Runs before the
	// structural frame (correct by construction) is appended, and is skipped for
	// the structural-only header (no prose to ground). Zero-LLM, every provider.
	if (!structuralOnly && !isTruthyEnvFlag(process.env.PIT_NO_SUMMARY_GROUNDING)) {
		summary = groundSummaryPaths(summary, lists, cwd).summary;
	}
	summary += formatFileOperations(lists);

	if (!firstKeptEntryId) {
		throw new Error("First kept entry has no UUID - session may need migration");
	}

	const details: CompactionDetails = {
		readFiles: lists.readFiles,
		modifiedFiles: lists.modifiedFiles,
	};
	if (lists.searches.length > 0) details.searches = lists.searches;
	if (lists.shellCmds.length > 0) details.shellCmds = lists.shellCmds;
	if (lists.mcpCalls.length > 0) details.mcpCalls = lists.mcpCalls;

	// File digests: a symbol outline of touched files at compaction time, so the
	// post-compaction model recalls the CURRENT shape of what it worked on without a
	// re-read. The collection was kicked off before the summarization LLM calls
	// (see digestsPromise above); only the await lands here.
	if (digestsPromise) {
		const digests = await digestsPromise;
		if (Object.keys(digests).length > 0) {
			details.fileDigests = digests;
			summary += `\n${formatFileDigests(digests)}`;
		}
	}

	// recall_history footer: announce that the summarized window is recoverable
	// on demand. The tool answers gracefully when there is no compacted history,
	// so the footer is unconditional (per default-native policy). Skipped for the
	// structural-only header (no prose window was summarized) and on opt-out.
	if (!structuralOnly && !isTruthyEnvFlag(process.env.PIT_NO_RECALL_HISTORY)) {
		summary += "\n[Details from the summarized window are retrievable via recall_history({ query }).]";
	}

	return {
		summary,
		firstKeptEntryId,
		tokensBefore,
		details,
	};
}

/**
 * Generate a summary for a turn prefix (when splitting a turn).
 */
async function generateTurnPrefixSummary(
	messages: AgentMessage[],
	model: Model<any>,
	reserveTokens: number,
	apiKey: string | undefined,
	headers?: Record<string, string>,
	signal?: AbortSignal,
	thinkingLevel?: ThinkingLevel,
	streamFn?: StreamFn,
	serialized?: SerializedWindow,
): Promise<string> {
	const maxTokens = summarizationMaxTokens(model, reserveTokens, 0.5); // Smaller budget for turn prefix
	const llmMessages = serialized?.llm ?? convertToLlm(messages);
	const conversationText = serializeConversation(llmMessages);
	const promptText = `<conversation>\n${conversationText}\n</conversation>\n\n${TURN_PREFIX_SUMMARIZATION_PROMPT}`;

	return runSummarization(
		model,
		promptText,
		maxTokens,
		apiKey,
		headers,
		signal,
		thinkingLevel,
		streamFn,
		"Turn prefix summarization failed",
	);
}
