/**
 * Calculate total context tokens from usage.
 * Uses the native totalTokens field when available, falls back to computing from components.
 */
export function calculateContextTokens(usage: Usage): number {
	return usage.totalTokens || usage.input + usage.output + usage.cacheRead + usage.cacheWrite;
}

/**
 * Get usage from an assistant message if available.
 * Skips aborted and error messages as they don't have valid usage data.
 */
function getAssistantUsage(msg: AgentMessage): Usage | undefined {
	if (msg.role === "assistant" && "usage" in msg) {
		const assistantMsg = msg as AssistantMessage;
		if (assistantMsg.stopReason !== "aborted" && assistantMsg.stopReason !== "error" && assistantMsg.usage) {
			return assistantMsg.usage;
		}
	}
	return undefined;
}

/**
 * Find the last non-aborted assistant message usage from session entries.
 */
export function getLastAssistantUsage(entries: SessionEntry[]): Usage | undefined {
	for (let i = entries.length - 1; i >= 0; i--) {
		const entry = entries[i];
		if (entry.type === "message") {
			const usage = getAssistantUsage(entry.message);
			if (usage) return usage;
		}
	}
	return undefined;
}

export interface ContextUsageEstimate {
	tokens: number;
	usageTokens: number;
	trailingTokens: number;
	lastUsageIndex: number | null;
}

/** Minimal tool surface for wire-size estimation (name + API schema). */
export interface WireToolSurface {
	name: string;
	description: string;
	parameters: unknown;
}

export interface WireEstimateInput {
	systemPromptChars: number;
	tools: WireToolSurface[];
	/** Messages not yet appended to session state (e.g. pending user turn). */
	pendingMessages?: AgentMessage[];
}

export interface WireUsageEstimate extends ContextUsageEstimate {
	/** Message-only portion (same as {@link ContextUsageEstimate.tokens}). */
	messageTokens: number;
	systemTokens: number;
	toolTokens: number;
	pendingTokens: number;
}

/**
 * Estimate of a tool-surface array, memoized by ARRAY reference. Tool schemas
 * are static per session, yet this ran a JSON.stringify over every schema on
 * every presend estimate. Callers that reuse the same surfaces array (see
 * `_wireToolsForEstimate`) pay the stringify once; throwaway arrays miss and
 * are GC'd with their entry.
 */
const toolSurfaceTokensCache = new WeakMap<object, number>();

/** Estimate serialized tool-definition payload on the wire. */
export function estimateToolSurfaceTokens(tools: WireToolSurface[]): number {
	const cached = toolSurfaceTokensCache.get(tools);
	if (cached !== undefined) return cached;
	let denseChars = 0;
	for (const tool of tools) {
		denseChars += tool.name.length + tool.description.length + JSON.stringify(tool.parameters).length;
	}
	const estimate = Math.ceil(denseChars / CHARS_PER_TOKEN_DENSE);
	toolSurfaceTokensCache.set(tools, estimate);
	return estimate;
}

/**
 * Estimate full provider wire size: session messages + system prompt + tool
 * schemas + any pending (not-yet-appended) messages.
 */
export function estimateWireTokens(messages: AgentMessage[], input: WireEstimateInput): WireUsageEstimate {
	const messageEstimate = estimateContextTokens(messages);
	let pendingTokens = 0;
	if (input.pendingMessages) {
		for (const message of input.pendingMessages) {
			pendingTokens += estimateTokens(message);
		}
	}
	const systemTokens = Math.ceil(input.systemPromptChars / CHARS_PER_TOKEN_PROSE);
	const toolTokens = estimateToolSurfaceTokens(input.tools);
	const tokens = messageEstimate.tokens + pendingTokens + systemTokens + toolTokens;
	return {
		...messageEstimate,
		messageTokens: messageEstimate.tokens,
		systemTokens,
		toolTokens,
		pendingTokens,
		tokens,
	};
}

function getLastAssistantUsageInfo(messages: AgentMessage[]): { usage: Usage; index: number } | undefined {
	for (let i = messages.length - 1; i >= 0; i--) {
		const usage = getAssistantUsage(messages[i]);
		if (usage) return { usage, index: i };
	}
	return undefined;
}

/**
 * Estimate context tokens from messages, using the last assistant usage when available.
 * If there are messages after the last usage, estimate their tokens with estimateTokens.
 */
export function estimateContextTokens(messages: AgentMessage[]): ContextUsageEstimate {
	const usageInfo = getLastAssistantUsageInfo(messages);

	if (!usageInfo) {
		let estimated = 0;
		for (const message of messages) {
			estimated += estimateTokens(message);
		}
		return {
			tokens: estimated,
			usageTokens: 0,
			trailingTokens: estimated,
			lastUsageIndex: null,
		};
	}

	const usageTokens = calculateContextTokens(usageInfo.usage);
	let trailingTokens = 0;
	for (let i = usageInfo.index + 1; i < messages.length; i++) {
		trailingTokens += estimateTokens(messages[i]);
	}

	return {
		tokens: usageTokens + trailingTokens,
		usageTokens,
		trailingTokens,
		lastUsageIndex: usageInfo.index,
	};
}

/**
 * Compute a dynamic reserve that scales with context window size.
 * - Small windows (≤200k): 10% of window, or the configured reserve.
 * - Large windows (>200k): the configured reserve, a 20k floor, AND a 2.5%
 *   floor. The percentage floor matters for very large windows: a flat 20k on a
 *   1M window is only 2% (trigger at ~98%, dangerously close to the hard limit
 *   if the token estimate runs low); 2.5% keeps ~25k headroom (~97.5%), leaving
 *   slack for estimation error before the model rejects on overflow.
 */
export function computeDynamicReserve(contextWindow: number, configuredReserve: number): number {
	if (!Number.isFinite(contextWindow) || contextWindow <= 0) return 0;
	const rawReserve =
		contextWindow > 200_000
			? Math.max(configuredReserve, 20_000, Math.ceil(contextWindow * 0.025))
			: Math.max(configuredReserve, Math.floor(contextWindow * 0.1));
	const maxUsableReserve = Math.max(1, Math.floor(contextWindow * 0.5));
	return Math.min(rawReserve, maxUsableReserve);
}

/**
 * Effective verbatim-retention budget for findCutPoint.
 *
 * The configured `keepRecentTokens` (default 20k) is a flat floor. On a large
 * window the trigger scales (computeDynamicReserve) but a flat 20k retention
 * would discard ~950k of raw recent messages in favor of a lossy summary. Scale
 * retention to 10% of the window above 200k (same threshold/percentage as the
 * reserve), keeping the configured value as a floor. Windows ≤200k are byte-
 * identical (10% of 200k == the 20k default floor).
 */
export function effectiveKeepRecentTokens(configured: number, contextWindow?: number): number {
	if (!contextWindow || contextWindow <= 200_000) {
		return configured;
	}
	return Math.max(configured, Math.floor(contextWindow * 0.1));
}

/** Tokens assumed for the summary + structural frame a compaction inserts. */
const COMPACTION_SUMMARY_TOKEN_BUDGET = 8_000;
/** Never retain less verbatim recent context than this, whatever the pressure. */
const ADAPTIVE_KEEP_MIN_FLOOR = 8_000;

/**
 * Adaptive verbatim-retention budget for a SINGLE compaction pass.
 *
 * The post-compaction context is ≈ kept-recent + summary. When that lands back
 * above the re-check threshold (the soft threshold, or the hard one on windows
 * too small for a positive soft band), runAutoCompaction used to run a SECOND
 * full pipeline — 1-2 extra LLM calls. On small windows the default keep alone
 * exceeds the target, so the second pass was structural, not incidental. This
 * shrinks the keep so one pass suffices: keep ≤ target − summary budget,
 * floored at max(8k, keepRecentTokens/2) so quality never collapses (the
 * second pass remains as fallback when the floor wins). Returns undefined when
 * no shrink is needed — normal windows are byte-identical to the legacy path.
 */
export function adaptiveKeepRecentTokens(contextWindow: number, settings: CompactionSettings): number | undefined {
	if (!Number.isFinite(contextWindow) || contextWindow <= 0) return undefined;
	const reserve = computeDynamicReserve(contextWindow, settings.reserveTokens);
	const effectiveKeep = effectiveKeepRecentTokens(settings.keepRecentTokens, contextWindow);
	const hardThreshold = contextWindow - reserve;
	const softThreshold = hardThreshold - effectiveKeep;
	// Mirror the post-compaction re-check: shouldCompactSoft only fires with a
	// positive soft threshold; otherwise the hard threshold is the bar.
	const target = softThreshold > 0 ? softThreshold : hardThreshold;
	const keepMax = target - COMPACTION_SUMMARY_TOKEN_BUDGET;
	if (keepMax >= effectiveKeep) return undefined;
	const floor = Math.max(ADAPTIVE_KEEP_MIN_FLOOR, Math.floor(settings.keepRecentTokens / 2));
	const adapted = Math.min(effectiveKeep, Math.max(floor, keepMax));
	return adapted < effectiveKeep ? adapted : undefined;
}

/** Absolute minimum floor for the proactive prune; below this the cache cost is not worth it. */
const PROACTIVE_PRUNE_MIN_FLOOR = 64_000;

/**
 * Context size (tokens) below which the proactive stale-tool-output prune is NOT
 * worth running. The prune rewrites old messages, which busts the provider
 * prompt cache — only pay that when there is real pressure.
 *
 * A flat floor over-prunes on very large windows: at 64k a 1M-window session is
 * ~6% full, so pruning there churns the cache while ~94% of room remains. Scale
 * the floor to 25% of the window, keeping 64k as an absolute minimum (windows up
 * to 256k stay at 64k, byte-identical to the old behaviour). A positive explicit
 * `override` (PIT_PROACTIVE_PRUNE_FLOOR) always wins.
 */
export function proactivePruneFloor(contextWindow: number, override?: number): number {
	if (override !== undefined && Number.isFinite(override) && override > 0) return override;
	const scaled = Number.isFinite(contextWindow) && contextWindow > 0 ? Math.floor(contextWindow * 0.25) : 0;
	return Math.max(PROACTIVE_PRUNE_MIN_FLOOR, scaled);
}

/** Hysteresis threshold: only re-compact if deficit grew by this many tokens since last compaction. */
const COALESCING_THRESHOLD_TOKENS = 8192;

/**
 * Check if compaction should trigger based on context usage.
 * Uses adaptive reserve scaling and hysteresis to avoid compaction churn.
 *
 * @param lastCompactionDeficit - deficit at last compaction trigger (0 if none). Callers
 *   should persist this value and pass it back on subsequent checks.
 */
export function shouldCompact(
	contextTokens: number,
	contextWindow: number,
	settings: CompactionSettings,
	lastCompactionDeficit = 0,
): boolean {
	if (!settings.enabled) return false;
	if (!Number.isFinite(contextWindow) || contextWindow <= 0) return false;
	const reserve = computeDynamicReserve(contextWindow, settings.reserveTokens);
	const threshold = contextWindow - reserve;
	if (contextTokens <= threshold) return false;
	const deficit = contextTokens - threshold;
	if (lastCompactionDeficit === 0) return true;
	return deficit > lastCompactionDeficit + COALESCING_THRESHOLD_TOKENS;
}

/**
 * Soft (predictive) trigger: fire compaction ~one `keepRecentTokens` window
 * BEFORE the hard threshold. Run in the background while the user reads the
 * just-finished turn, so the summary is ready before they send the next prompt
 * (no visible compaction wait). The hard `shouldCompact` stays as the
 * synchronous fallback for turns that jump straight past it.
 *
 * Returns false once at/over the hard threshold — there the caller must compact
 * synchronously, not defer.
 */
export function shouldCompactSoft(contextTokens: number, contextWindow: number, settings: CompactionSettings): boolean {
	if (!settings.enabled) return false;
	if (!Number.isFinite(contextWindow) || contextWindow <= 0) return false;
	const reserve = computeDynamicReserve(contextWindow, settings.reserveTokens);
	const hardThreshold = contextWindow - reserve;
	if (contextTokens > hardThreshold) return false; // hard path owns this
	// Scale the predictive band with the window (same 10%-above-200k rule as retention) so a
	// large window gets a real head-start instead of a flat 20k sliver below the hard threshold
	// that a single big turn would jump straight past into the synchronous path. Windows ≤200k
	// are byte-identical (effectiveKeepRecentTokens returns the raw keepRecentTokens).
	const softThreshold = hardThreshold - effectiveKeepRecentTokens(settings.keepRecentTokens, contextWindow);
	return softThreshold > 0 && contextTokens > softThreshold;
}

